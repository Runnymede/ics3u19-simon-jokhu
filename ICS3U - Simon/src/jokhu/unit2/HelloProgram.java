public class HelloProgram {
/**
 * Beginning of HelloProgram
 * @param args
 */
	public static void main(String[] args) {
		int counter= 0;
		//Prints hello 10 times with the use of a counter
		while (counter <10) {
			counter++;
			System.out.println("Hello");
		}

	}

}
